# Fallback to SQLite if PostgreSQL is not reachable during runserver
import sys
if 'runserver' in sys.argv:
    import socket
    db_host = os.environ.get('DB_HOST', 'localhost')
    db_port = os.environ.get('DB_PORT', '5432')
    try:
        with socket.create_connection((db_host, int(db_port)), timeout=1):
            pass
    except Exception:
        import warnings
        warnings.warn(
            f"PostgreSQL at {db_host}:{db_port} is not reachable. "
            "Falling back to SQLite database for development server to prevent startup crash."
        )
        DATABASES = {
            'default': {
                'ENGINE': 'django.db.backends.sqlite3',
                'NAME': BASE_DIR / 'db.sqlite3',
            }
        }


